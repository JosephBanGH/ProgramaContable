private double convertirUnidad(double valor, String tipoOrigen, String tipoDestino) {
    // Conversión de tiempo
    if (tipoOrigen.equals("HORA")) {
        if (tipoDestino.equals("DIA")) {
            return valor / 24;
        } else if (tipoDestino.equals("MES")) {
            return valor / (24 * 30);
        } else if (tipoDestino.equals("AÑO")) {
            return valor / (24 * 365);
        }
    } else if (tipoOrigen.equals("DIA")) {
        if (tipoDestino.equals("HORA")) {
            return valor * 24;
        } else if (tipoDestino.equals("MES")) {
            return valor / 30;
        } else if (tipoDestino.equals("AÑO")) {
            return valor / 365;
        }
    } else if (tipoOrigen.equals("MES")) {
        if (tipoDestino.equals("HORA")) {
            return valor * 24 * 30;
        } else if (tipoDestino.equals("DIA")) {
            return valor * 30;
        } else if (tipoDestino.equals("AÑO")) {
            return valor / 12;
        }
    } else if (tipoOrigen.equals("AÑO")) {
        if (tipoDestino.equals("HORA")) {
            return valor * 24 * 365;
        } else if (tipoDestino.equals("DIA")) {
            return valor * 365;
        } else if (tipoDestino.equals("MES")) {
            return valor * 12;
        }
    }
    // Conversión de distancia
    if (tipoOrigen.equals("METRO")) {
        if (tipoDestino.equals("KILOMETRO")) {
            return valor / 1000;
        }
    } else if (tipoOrigen.equals("KILOMETRO")) {
        if (tipoDestino.equals("METRO")) {
            return valor * 1000;
        }
    }
    return valor;  // Si las unidades son las mismas
}

