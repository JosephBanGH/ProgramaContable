    private void btnEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaluarActionPerformed
        try {
            double valorAdquisicion = Double.parseDouble(txtAquisicion.getText());
            double valorResidual = Double.parseDouble(txtResidual.getText());
            double vidaUtil = Double.parseDouble(txtVida.getText());
            double unidadesProducidas = Double.parseDouble(txtUnidades.getText());

            String tipoVidaUtil = combTipo.getSelectedItem().toString();
            String tipoUnidadesProducidas = combTipo2.getSelectedItem().toString();

            // Verificar si los combobox tienen un valor válido seleccionado
            if (tipoVidaUtil.equals("----") || tipoUnidadesProducidas.equals("----")) {
                throw new IllegalArgumentException("Por favor, seleccione unidades válidas.");
            }

            // Asignar categorías a las unidades
            int categoriaVidaUtil = (tipoVidaUtil.equals("HORA") || tipoVidaUtil.equals("DIA") || tipoVidaUtil.equals("MES") || tipoVidaUtil.equals("AÑO")) ? 0 : 1;
            int categoriaUnidadesProducidas = (tipoUnidadesProducidas.equals("HORA") || tipoUnidadesProducidas.equals("DIA") || tipoUnidadesProducidas.equals("MES") || tipoUnidadesProducidas.equals("AÑO")) ? 0 : 1;

            // Verificar si las categorías son diferentes
            if (categoriaVidaUtil != categoriaUnidadesProducidas) {
                throw new IllegalArgumentException("Por favor, seleccione unidades de la misma categoría (tiempo o medida) en ambos ComboBox.");
            }

            double importeDepreciable = valorAdquisicion - valorResidual;
            double depreciacionAnual = 0;
            double depreciacionMensual = 0;

            vidaUtil = convertirUnidad(vidaUtil, tipoVidaUtil, "HORA"); // Convertir vida útil a horas
            unidadesProducidas = convertirUnidad(unidadesProducidas, tipoUnidadesProducidas, "HORA"); // Convertir unidades producidas a horas

            // Calcular depreciación anual y mensual en base a las horas
            if (vidaUtil > 0) {
                depreciacionAnual = importeDepreciable / (vidaUtil / (24 * 365));
                depreciacionMensual = depreciacionAnual / 12;
            } else if (vidaUtil == 0) {
                throw new IllegalArgumentException("Vida útil no puede ser cero.");
            }

            txtImporte.setText(String.valueOf(importeDepreciable));
            txtAnual.setText(String.valueOf(depreciacionAnual));
            txtMensual.setText(String.valueOf(depreciacionMensual));

            JOptionPane.showMessageDialog(this, "Cálculo completado con éxito");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEvaluarActionPerformed

